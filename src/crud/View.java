    public void combobox(){
        jComboBox1.removeAllItems();
        try {
            Statement st = (Statement) Conn.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM plano");
            while(rs.next()){
                jComboBox1.addItem(rs.getString("id") + " - " + rs.getString("nome"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error: " + e.getMessage());
        }
    }

    public void table(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Nome");
        tbl.addColumn("CPF");
        tbl.addColumn("Plano");
        tbl.addColumn("Pre√ßo");
        
        try {
            Statement st = (Statement) Conn.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT i.nome, i.cpf_cliente, p.nome, p.preco FROM inscricao as i inner join plano as p on i.fk_plano = p.id ");
            while(rs.next()){
                tbl.addRow(new Object[]{
                    rs.getString("nome"),
                    rs.getString("cpf_cliente"),
                    rs.getString("p.nome"),
                    rs.getString("p.preco"),
                });
                jTable1.setModel(tbl);
            }
            JOptionPane.showMessageDialog(null, "Success");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error: " + e.getMessage());
        }
    }
    
